	<h1>Cold Spring Park Landfill Cleanup</h1>
	<p>Decades ago, part of what is now Cold Spring Park was used as a trash dump. For years, large pieces of metal debris littered the edge of a small, seasonal wetland area in the park. On the NewtonSERVES Day of Community Service on May 15, 2011, the Conservators sponsored a project to remove most of this debris. Twenty volunteers, including many members of Boy Scout Troop 205/242, spent most of a rainy Sunday afternoon digging, bagging, and lifting debris, and hauled off an amazing amount. A few big pieces just couldn't be budged, but the pond has never looked better.</p><br>
	<p>The Conservators would like to thank all of the volunteers, and especially crew leaders Andrew Hutner (a member of Troop 205/242) and Shawn Konary, for their work to improve this park.</p>
